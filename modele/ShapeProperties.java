package com.example.tp2_android_drawing.modele;

public class ShapeProperties {
    private float x;
    private float y;

    public ShapeProperties(float x,float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
